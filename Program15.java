//WAP to swap 2 variable values using 3rd variable
class Program15
{
    public static void main(String [] args)
    {
    int a=10;
    int y=55;
    int m;
     System.out.println("Before swapping");
     System.out.println("a: "+a);
     System.out.println("y :"+y);
     System.out.println("--------------");
     m=a;
     a=y;
     y=m;
     System.out.println("Before swapping");
     System.out.println("a: "+a);
     System.out.println("y :"+y);


    }

}